# 商品名の正規化関数（空白・全角記号などを統一）
def normalize(name: str) -> str:
    import re
    name = name.strip()
    name = name.replace("（", "(").replace("）", ")")
    name = re.sub(r"\s+", "", name)
    return name.lower()

# 分類関数（未分類は "未分類" と返す）
def categorize(name: str) -> str:
    key = normalize(name)
    return category_map.get(key, "未分類")


